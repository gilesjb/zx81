unsigned char *dfiles[] = {lbuf0, rbuf0, lbuf1, rbuf1};

// 0 <= x <= 19, 0 <= y <= 14
#define PTR(buffer, x) (buffer + 16 + x) 
#define SHIFT *px1 = W; *px2 = L; px1 += 33; px2 += 33

static void shift(unsigned char *buffer, char x2) {
    char x1 = buffer[0];
    unsigned char *px1 = PTR(buffer, x1), *px2 = PTR(buffer, x2);
    SHIFT; SHIFT; SHIFT; SHIFT; SHIFT; SHIFT; SHIFT; SHIFT; SHIFT;
    buffer[0] = x2;
}

unsigned char **dfile = (unsigned char **) 0x400C;
unsigned char *frames = (unsigned char *) 0x4034;

int main() {
    unsigned char *bfile = *dfile;
    unsigned char current_frame = *frames;
    unsigned char ibuf = 0;

    char x_ = 39, dx_ = -1; // using sub-char x coords
    char y = 14;

    for (;;) {
        if (y < 0) y = 14;

        while (current_frame == *frames) {
            intrinsic_halt();
        }
        current_frame = *frames;
        *dfile = dfiles[ibuf] + y + 1; // add 1 for curr_x byte at beginning

        ibuf ^= 1;
        if ((current_frame & 0x0f) == 0) {
            ibuf ^= 2;
        }

        if (x_ == 0) {
            dx_ = 1;
            y--;
        } else if (x_ == 39) {
            dx_ = -1;
            y--;
        } else {
            shift(dfiles[ibuf], (x_ + dx_) / 2);
        }

        x_ += dx_;

        if (in_Inkey()) {
            *dfile = bfile;
            return dfiles[ibuf];
        }
    }
}
